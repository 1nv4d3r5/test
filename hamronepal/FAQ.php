            <h3>What are some basic Nepali customs that I need to know?</h3>
			<ul>
<li>When visiting a temple, always circumambulate it is a clockwise direction.</li>
<li>Take off your shoes before entering a temple or a Nepalese home.</li>
<li>Entrance to some temples is forbidden, so look out for such notices.</li>
<li>Seek permission to take photographs inside temples or of religious ceremonies.</li>
<li>You can accept a handshake offered by either a male or a female but never offer your hand first.</li>
<li>Show decency in dress and avoid any show of public affection.</li>
</ul>
<p><br/></p>
            	
            <h3>How do I get around in the cities and to other destinations?</h3>
			<p>There are metered taxis, buses, battery-operated three-wheelers and rickshaws in the cities. Long routes are served by buses. Nepal does not have a railway system connecting cities. The only other travel option is by plane, and Nepal is well connected by air.</p>
<p>There are Intercity Tourist Buses to popular tourist destinations like Pokhara, Chitwan and Lumbini from Kantipath in Kathmandu, while you can board a bus to Jiri and Dhunche at the New Bus Park.</p>
            	
            <h3>How reliable is the post office in Nepal?</h3>
			<p>The post office is reliable. Express Mail Service (EMS) is available at the General Post Office located at Sundhara near the Dharahara tower in Kathmandu and at Thamel, Basantapur and airport postal counters in the capital. Kathmandu has the only Post Restante of the country.</p>
            	
            <h3>How are communication facilities in Nepal?</h3>
			<p>Mobile coverage is wide in Nepal, even in the rural areas. Internet facilities are available in hotels and cyber cafes in all major cities. Apart from the urban centers, popular trekking destinations &#8211; the Annapurna and Everest region &#8211; also have modern communications facilities.</p>
            	
            <h3>Accommodation</h3>
			<p>There is accommodation ranging from 5-star chain hotels and resorts to comfortable lodges in Kathmandu and major tourist destinations. The hotels offer specialty restaurants, conference facilities, exclusive health clubs and business centers. You also have the option of home-stay where you can stay in local houses with the local people and enjoy their traditional food.</p>
            	
            <h3>What kind of food is available in Nepalese restaurants?</h3>
			<p>Kathmandu is a melting pot of international cuisine. Dining out is, therefore, a pleasure. The Nepali staple is rice, dal (lentils) and curry. Apart from Nepalese cuisine, there are restaurants serving Continental, Indian, Chinese, Mexican, Japanese, Italian and Korean food. Fast foods like burgers, pizzas are found everywhere. There are two KFC outlets in Kathmandu.</p>
            	
            <h3>What souvenirs can I take home?</h3>
			<p>Popular among visitors are handmade apparels like woolen sweaters, jackets, trousers and caps. And the Pashmina shawl is a highly coveted item. Then there are ethnic and contemporary carpets, gems and jewelry, metal and wooden products, Khukuri (the curved metal knife, music CDs, Nepali paper products, pottery, spices, tea and Thangka paintings to take home.<br />
There are exclusive showrooms at Durbar Marg, Asan, Thamel, Patan and Bhaktapur Durbar Square area which specialize in these items.</p>
