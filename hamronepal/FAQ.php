<h3>From the airport, how do I get to the city centre?</h3>
			<p>There are taxi-drivers, travel agents and hoteliers waiting outside the airport terminal to offer their services to you. Pre-paid taxi service is also available at the airport.</p>
            	
            <h3>Is there an airport tax in Nepal?</h3>
			<p>Yes. It is already included in your ticket. But on domestic routes, a separate airport tax of Rs.200 is charged before boarding.</p>
            	
            <h3>Do I need a visa to enter Nepal?</h3>
			<p>All foreign nationals, except Indians, need visas to enter Nepal. Multiple entry visas for 15 days (US$ 25 or equivalent convertible currency), 30 days (US$ 40) or 90 days (US$ 100) can be obtained from any Nepalese embassy or consulate. You can also get a visa on arrival at the Tribhuvan International Airport , Kathmandu or at the Immigration Office at the entry points of Nepal.</p>
<p>Tourist visas can be extended for a period of 120 days at the Immigration Department in Kathmandu or Pokhara.</p>
<p>However, nationals of the following countries will not get visa on arrival at the immigration entry points of Nepal: Afghanistan, Cameroon, Ethiopia, Ghana, Iraq, Liberia, Nigeria, Palestine, Somalia, Swaziland and Zimbabwe. They need to obtain visas from Nepalese embassies or diplomatic missions in their respective countries prior to their arrival in Nepal.</p>
<p>Gratis (Free) Visa for 30 days is available only for nationals of South Asian countries like Bangladesh, Bhutan, Maldives, Pakistan and Sri Lanka for the first visit in one visa year (January to December). However, a visa fee is required for its extension beyond the 30 days.</p>
<p>Indian nationals do not require a visa to enter Nepal.</p>
