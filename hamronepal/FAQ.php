<strong>How do I get to Nepal?</strong></br>
Tribhuvan International Airport (TIA) in Kathmandu is the only international airport in Nepal. The national flag carrier, Nepal Airlines, and other international airlines directly connect Kathmandu with major cities around the world. Alternatively, you can travel to Nepal via India.<br/></br>
<strong>While coming by land, what are the entry points?</strong><br/>
All visitors coming to Nepal by land can enter through any of these entry points on the India- Nepal Border:</br>
Pani Tanki/Kakarbhitta</br>
Raxual/Birgunj</br>
Sunauli/Belahiya</br>
Rupaidiya/ Nepalgunj</br>
Mohana/Dhangadi</br>
Banbasa/Mahendranagar, Gaddachauki and</br>
Khasa,Liping/ Tatopani on the Tibet,China-Nepal border respectively.</br>
Overland tourists entering the country with their vehicles must possess an international carnet or complete customs formalities.</br></br>
<strong>What are the Air Accesses to Nepal ?</strong><br/>
BY AIR:<br/>
Nepal Airlines is the national flag carrier of Nepal with flights to/from Delhi, Kuala Lumpur, Dubai, Bangkok, Doha and Hong Kong. Other international airlines operating from/ to Kathmandu are Air Arabia (Sharjah), Air China (Lhasa, Chengdu), ArkeFly (Amsterdam), Bahrain Air (Bahrain), Biman Bangladesh (Dhaka), Buddha Air (Paro, Lucknow), China Eastern Airlines (Kunming), China Southern Airlines (Guanzhou), Dragon Air (Hong Kong), Druk Air (Delhi, Paro), Etihad Airways (Abu Dhabi), Flydubai (Dubai), GMG Airlines (Dhaka), Gulf Air (Bahrain), Indian Airlines (Delhi, Kolkotta, Varanasi), Indigo Airlines (Delhi), Jet Airways (Delhi, Mumbai), Jet Lite (Delhi), Kingfisher Airlines ( Delhi), Korean Air (Seoul), Oman Air (Muscat), Pakistan International Airlines (Karachi, Islamabad) , Qatar Airways (Doha), Silk Air (Singapore), Spicejet (Delhi), Thai Airways (Bangkok) and United Airways (Dhaka) .</br></br>

