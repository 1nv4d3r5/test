            <h3>What documents must Indian nationals present at the entry point?</h3>
			<p>As per the Nepalese Immigration, Indian Nationals Traveling to Nepal must posses any One of the following documents-</p>
<ol>
<li>Passport</li>
<li>Driving License with photo</li>
<li>Photo Identity card issued by a Government Agency</li>
<li>Ration Card with Photo</li>
<li>Election Commission Card with Photo</li>
<li>Identity Card issued by Embassy of India in Kathmandu</li>
<li>Identity Card with Photo issued by Sub- Divisional Magistrate or any other officials above his rank</li>
</ol>
<p>However, please check with your nearest travel agents for documents required by the Indian Immigration for Indians traveling to Nepal.</p>
            	
            <h3>What are needed to get a visa?</h3>
			<p>A valid passport and one passport size photo with a light background . Immigration Department has not specified the size of the passport-size photo.  Visa can be obtained only through payment of cash in the following currency : Euro, Swiss Franc, Pound Sterling, US dollar, Australian dollar, Canadian dollar, Hong-kong dollar, Singapore dollar and  Japanese Yen.</p>
<p>Credit Card, Indian currency or Nepalese currency are not accepted for payment of Visa fee.</p>
            	
            <h3>What to wear?</h3>
			<p>Light, loose garments are recommended in spring (March, April and May) and summer (June, July and August), while woolens and jackets or similar warm clothings are needed from November to February.</p>
            	
            <h3>What is the currency of Nepal?</h3>
			<p>The Nepalese currency is the rupee and is divided into 100 paisa. Bank notes come in denominations of Rs. 1, 2, 5, 10, 20, 50, 100, 500 and 1,000. Coins are of 1, 2 and 5 rupee denomination.</p>
            	
            <h3>Where can I exchange my money ?</h3>
			<p>It is illegal to exchange foreign currency with persons and organizations other than those authorized such as banks, hotels and licensed money changers. Nepalese banks do not accept Indian currency of denomination of Rs. 500 and 1000. You will find the exchange rates for different currencies in the Nepalese papers.<br />
In order to exchange surplus rupees at the time of your departure, please retain your encashment receipts.</p>
            	
            <h3>Are there ATMS in Nepal?</h3>
			<p>There are ATMS in Kathmandu, Pokhara and other big cities in Nepal that accept AMERICAN EXPRESS, VISA and MASTERCARD.</p>
            	
            <h3>Are credit cards accepted in Nepal?</h3>
			<p>Major hotels, restaurants and curio shops accept AMERICAN EXPRESS, VISA and MASTERCARD. You can contact the banks for credit card services.</p>
            	
            <h3>If I run out of money, how do I get access to more funds?</h3>
			<p>You can draw funds against your AMERICAN EXPRESS, VISA and MASTERCARD cards in Kathmandu. The service premiums for doing this, however, could be high. You can also have money wired directly to Kathmandu through any major bank within two to three working days.</p>
            	
            <h3>Is the international driving license accepted in Nepal?</h3>
			<p>Yes.</p>
            	
            <h3>What inoculations are recommended before visiting Nepal?</h3>
			<p>Although specific immunization is not required to enter Nepal, it is, however, best to protect yourself against diseases such hepatitis, meningitis and Japanese encephalitis. Malaria has been reported in the Terai plains of Nepal, so take precautions against being bitten by mosquitoes. Please notify the authorities or the hotel of any symptoms of bird or swine flu or any endemic.</p>
            	
            <h3>What health precautions need to be taken?</h3>
			<p>Use only bottled mineral water or boiled and filtered water only. Always wash your hands before eating. Do not eat unpeeled fruits or vegetables unless they have been thoroughly washed.</p>
            	
            <h3>When is the best time to travel in Nepal?</h3>
			<p>Because of its elevation, Nepal can be divided into three zones – the high mountains, the mid-hills and the flat plains called the Terai. The Everest, Langtang and Annpurna trekking destinations in the mountains are cool throughout the year. The mid-hills, including Kathmandu and Pokhara, are cool except in the summer months from May to July. Temperatures in these cities during the summer remain much cooler than in many cities of the region. Lumbini, Chitwan and Janakpur in the Terai plains are hot in summer (March –July) but cold in winter (December-February).</p>
<p>So if you are going trekking, the best months for walking are between September and November and between February and May when the weather is fair during the day and the temperatures do not drop rapidly during the night.</p>
            	
            <h3>If I need to see a doctor, where can I do so?</h3>
			<p>There are well-equipped general and specialized hospitals, nursing homes and private clinics manned by very competitive doctors in Kathmandu, Pokhara and other cities if you fall sick. Your hotel will provide good advice on the matter.</p>
            	
            <h3>What is Acute Mountain Sickness and how do you prevent it?</h3>
			<p>Acute Mountain Sickness (AMS) is caused by thin air at high altitudes starting from 3, 000 meters upwards and may even lead to death. The main precaution that needs to be taken while trekking is not to go up too high too fast. So the body should be given enough time to acclimatize.</p>
<p>If you suffer from initial symptoms like headaches, shortness of breath, fatigue, nausea, inability to sleep, swelling of the face, hands and feet and loss of appetite, descend to a lower elevation immediately, and seek medical help.</p>
            	
            <h3>Are there clinics to treat AMS on trekking routes?</h3>
			<p>The Himalayan Rescue Association (HRA), a non-profit organization that works to prevent casualties from AMS, operates a permanent aid post in Manang which is managed by volunteer doctors from HRA. It also operates a small aid post in the Khumbu village of Pheriche at 4,280 m during the trekking season by volunteer doctors. HRA also operates a camp at Everest Base Camp during the mountaineering season.<br />
For more information,<br />
<strong>Contact</strong>: 01-4440293</p>
            	
            <h3>Is travel insurance necessary to go trekking in Nepal?</h3>
			<p>Helicopter services are available should you fall sick or meet with an accident while trekking. However, such rescue services are expensive. So comprehensive travel insurance is advised to cover emergencies like helicopter rescue and medical treatment.</p>
            	
            <h3>How safe is Nepal?</h3>
			<p>It is safe to take a walk around the city from morning to late evening until the shops are closed. But you are advised not to trek alone. Trekking with a guide from a registered agency is the best security. Do not display your cash or expensive items. Always lock your room and baggage.</p>
<p>The Tourist Police at Bhrikuti Mandap is there to help you with security and travel-related problems. If any valuable item is lost, you can contact the Tourist Police to prepare documents for theft to claim insurance.<br />
Tel: 4247041<br />
(email: <a href="mailto:policetourist@yahoo.com">policetourist@yahoo.com</a>/<a href="mailto:policetourist@nepalpolice.gov.np">policetourist@nepalpolice.gov.np</a>)</p>
            	
            <h3>Are there public toilets in Nepal?</h3>
			<p>Public toilets can be used for a small fee. But visitors are advised to use the toilets at the shopping malls, stores and restaurants which are cleaner.</p>
            	
            <h3>What is the country code of Nepal?</h3>
			<p>The country code for Nepal is 977 and the area code for Kathmandu is 01, for Pokhara 061, Chitwan 056 and Lumbini 071.</p>
            	
            <h3>What is the time difference in Nepal?</h3>
			<p>Nepali time is GMT plus 5 hours 45 minutes.</p>
            	
            <h3>What is Trekkers' Information Management System (TIMS)?</h3>
			<p><strong>Where and how to obtain TIMS Card?</strong></p>
<p>Tourists of all nationalities including Indians, who are interested to visit general trekking areas of Nepal, are required to receive TIMS Card through one of the following offices:</p>
<ul>
<li>Kathmandu (NTB office, TAAN office and Government registered trekking companies)</li>
<li>Pokhara (NTB office, TAAN office and Government registered trekking companies)</li>
<li>To obtain a TIMS Card you need a photo copy of your passport details and two passport-size photographs.</li>
<li>20 US $ equivalent Nepalese Rupees must be paid to obtain TIMS Card from NTB offices or TAAN offices which issue TIMS card only for Free Individual Trekkers (FIT) who do not take the services of both Guide and Porter.</li>
<li>However, 10 US $ equivalent Nepalese Rupees must be paid for obtaining TIMS card for Group Trekkers (GT- who take the services of both Guide and Porter) only from registered trekking agencies in Nepal.</li>
<li>The TIMS card is non-transferable, non-endorsable and valid only for one entry for prescribed area and duration.</li>
</ul>
<p><strong>Opening hours</strong></p>
<ul>
<li>TIMS counter at NTB offices will remain open from Sunday to Friday from 10 am to 4 pm except Saturdays and Public Holidays when it is closed.</li>
<li>TIMS counter at TAAN opens seven days a week from 10 am to 5 pm . It is also open on Saturdays and Public Holidays from 10 am to 12 pm.</li>
<li>During October, November and December working hours are from 10 am to 4 pm.</li>
</ul>
